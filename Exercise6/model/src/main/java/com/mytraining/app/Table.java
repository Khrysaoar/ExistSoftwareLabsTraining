package com.mytraining.app;

import java.util.*;
 
public class Table {
	private int rows;
	private int cols;
	
	public Table() {}
	
	public void setRows(int rows) {
		this.rows = rows;
	}

	public void setCols(int cols) {
		this.cols = cols;
	}
	
	public int getRows() {
		return rows;
	}

	public int getCols() {
		return cols;
	}
}